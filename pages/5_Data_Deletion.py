import streamlit as st

st.set_page_config(layout="wide")
st.title("Data Deletion Instructions")

st.markdown("""
In accordance with Facebook Platform rules, we provide the following instructions for data deletion.

## 1. Data Collection Summary
The Video Game Stats Tracker ("the Service") is a personal tool primarily used by the administrator. The only user data collected from third-party accounts (such as Facebook) is the user's **email address** and **basic public profile information** required for authentication purposes. We do not save or track personal user behaviors outside of the specific game statistics manually entered into the system.

## 2. How to Remove Your Data via Facebook
If you have logged into the Service using Facebook and wish to revoke access and remove the app's association with your account, please follow these steps:

1.  Go to your Facebook Account's **Settings & Privacy**. Click **Settings**.
2.  Look for **Apps and Websites** on the left-hand menu.
3.  Select **Video Game Stats Tracker** from the list of active apps.
4.  Click **Remove**.
5.  Confirm that you wish to remove the app.

Once removed, the application will no longer have access to your Facebook profile data.

## 3. Permanent Data Deletion Request
If you wish to ensure that your email address and any associated data are permanently deleted from our application's database, you may submit a request directly to the administrator.

Please send an email to **thebolgroup.llc@gmail.com** with the subject line **"Data Deletion Request"**. We will process your request and delete your data within 30 days.

## 4. Data Retention Policy
For the primary user (Administrator), data is retained indefinitely to maintain statistical history. For any other users, data is retained only as long as necessary to provide the service or until a deletion request is received.
""")